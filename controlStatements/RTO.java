package com.xworkz.controlStatements;

public class RTO {

	public static void main(String[] args) {
		int rtoNumber = 12;

		switch (rtoNumber) {

		case 29:
			System.out.println("Navanagar Bagalkot");
			break;

		case 48:
			System.out.println("Jamkhandi Bagalkot");
			break;

		case 63:
			System.out.println("Hubli");
			break;

		case 25:
			System.out.println("Dharwad");
			break;
			
		default: System.out.println("Please enter a valid RTO number.");

		}

	}

}
