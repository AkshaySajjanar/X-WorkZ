package com.xworkz.arrayExamples;

public class CollegeBlock {

	public static void main(String[] args) {

		System.out.println("Beginning");
		char collegeBlock[] = { 'A', 'B', 'C', 'D'};
		
		System.out.println("Size of the array "+collegeBlock.length);
		
		for (int i = 0; i < collegeBlock.length; i++) {
			System.out.println(collegeBlock[i]);
		}
		
		System.out.println("End of program");

	}

}
